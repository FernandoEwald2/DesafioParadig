namespace ArvoreBinariaApp
{

    // nome da classe Node é referencia a nó
    public class Node
    {
        public int Valor;
        public Node Esquerda;
        public Node Direita;

        public Node(int valor)
        {
            Valor = valor;
            Esquerda = null;
            Direita = null;
        }
    }

    

}
